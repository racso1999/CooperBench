def _style_axes(ax, ymax=100):
    ax.set_ylim(0, ymax)
    ax.set_yticks(range(0, ymax + 1, 20))
    ax.set_yticklabels([f"{y}%" for y in range(0, ymax + 1, 20)])
    ax.xaxis.grid(False)
    ax.tick_params(length=0)
    for s in ("top", "right", "left"):
        ax.spines[s].set_visible(False)
    ax.spines["bottom"].set_color(GRID)


# ----------------------------------------------------------------------------
# Fig 1 — primary endpoint (merge-clean) with Wilson CIs
# ----------------------------------------------------------------------------
def fig_primary():
    fig, ax = plt.subplots(figsize=(8.2, 4.6))
    x = range(len(ARMS))
    rates = [p[0] for p in PRIMARY]
    lo = [p[0] - p[1] for p in PRIMARY]
    hi = [p[2] - p[0] for p in PRIMARY]
    # winner deep forest, floor arms sage — encodes the merge-clean metric, not rank
    colors = [SAGE] * 5 + [FOREST_DEEP]
    ax.bar(x, rates, color=colors, width=0.62, zorder=3)
    ax.errorbar(x, rates, yerr=[lo, hi], fmt="none", ecolor=INK, elinewidth=1.4, capsize=4, capthick=1.4, zorder=4)
    for xi, r in zip(x, rates):
        ax.text(
            xi, r + max(hi) * 0.06 + 3, f"{r}%", ha="center", va="bottom", fontsize=11, fontweight="bold", color=INK
        )
    _style_axes(ax)
    ax.set_xticks(list(x))
    ax.set_xticklabels(ARMS, fontsize=9.5)
    ax.set_title(
        "Primary endpoint — merge-clean rate by protocol", fontsize=13.5, fontweight="bold", pad=12, loc="left"
    )
    ax.text(
        0,
        1.015,
        "Two independent patches combine without conflict (no-git coop, 18 validated pairs, Wilson 95% CI)",
        transform=ax.transAxes,
        fontsize=9.5,
        color=MUTED,
    )
    ax.axhline(13, color=AMBER, lw=1.1, ls=(0, (4, 3)), zorder=2)
    ax.text(len(ARMS) - 0.5, 15.5, "control floor 13%", ha="right", va="bottom", fontsize=8.5, color=BRONZE)
    fig.tight_layout()
    fig.savefig(OUT / "primary.png", dpi=150, bbox_inches="tight")
    plt.close(fig)


