# Fig 2 — primary vs secondary divergence
# ----------------------------------------------------------------------------
def fig_divergence():
    fig, ax = plt.subplots(figsize=(8.2, 4.6))
    x = list(range(len(ARMS)))
    w = 0.38
    prim = [p[0] for p in PRIMARY]
    sec = [s[0] for s in SECONDARY]
    ax.bar([xi - w / 2 for xi in x], prim, width=w, color=FOREST_DEEP, zorder=3, label="merge-clean (primary)")
    ax.bar([xi + w / 2 for xi in x], sec, width=w, color=AMBER, zorder=3, label="both_passed (secondary)")
    for xi in x:
        ax.text(xi - w / 2, prim[xi] + 2, f"{prim[xi]}", ha="center", va="bottom", fontsize=8.5, color=FOREST_DEEP)
        ax.text(xi + w / 2, sec[xi] + 2, f"{sec[xi]}", ha="center", va="bottom", fontsize=8.5, color=BRONZE)
    _style_axes(ax)
    ax.set_xticks(x)
    ax.set_xticklabels(ARMS, fontsize=9.5)
    ax.set_title(
        "Primary vs secondary — the solo_rescue divergence", fontsize=13.5, fontweight="bold", pad=12, loc="left"
    )
    ax.text(
        0,
        1.015,
        "designated_coder scores high on both_passed but not on merge-clean: its wins are solo_rescue, not merges",
        transform=ax.transAxes,
        fontsize=9.5,
        color=MUTED,
    )
    # annotate the gap on designated_coder
    ax.annotate(
        "40-pt gap\n= solo_rescue",
        xy=(4, 45),
        xytext=(3.05, 62),
        fontsize=8.5,
        color=BRONZE,
        ha="center",
        arrowprops=dict(arrowstyle="->", color=BRONZE, lw=1.1),
    )
    ax.legend(frameon=False, loc="upper left", fontsize=9.5, handlelength=1.1, bbox_to_anchor=(0.0, 0.95))
    fig.tight_layout()
    fig.savefig(OUT / "divergence.png", dpi=150, bbox_inches="tight")
    plt.close(fig)


# ----------------------------------------------------------------------------
# Fig 3 — failure taxonomy (stacked)
# ----------------------------------------------------------------------------
def fig_taxonomy():
    fig, ax = plt.subplots(figsize=(8.2, 4.8))
    x = list(range(len(ARMS)))
    bottom = [0.0] * len(ARMS)
    for j, (lbl, col) in enumerate(zip(TAXO_LABELS, TAXO_COLORS)):
        vals = [TAXONOMY[i][j] for i in range(len(ARMS))]
        ax.bar(x, vals, bottom=bottom, color=col, width=0.66, zorder=3, label=lbl, edgecolor=SURFACE, linewidth=1.2)
        # label the large segments
        for i, v in enumerate(vals):
            if v >= 12:
                ax.text(
                    i,
                    bottom[i] + v / 2,
                    f"{v}%",
                    ha="center",
                    va="center",
                    fontsize=8.5,
                    color="white" if col in (FOREST_DEEP, BROWN, BRONZE) else INK,
                    fontweight="bold",
                )
        bottom = [b + v for b, v in zip(bottom, vals)]
    _style_axes(ax)
    ax.set_xticks(x)
    ax.set_xticklabels(ARMS, fontsize=9.5)
    ax.set_title("Failure taxonomy by protocol", fontsize=13.5, fontweight="bold", pad=12, loc="left")
    ax.text(
        0,
        1.015,
        "Where each pair-run lands after the naive merge (validated set). Forest = honest pass; amber = solo_rescue",
        transform=ax.transAxes,
        fontsize=9.5,
        color=MUTED,
    )
    handles = [Patch(facecolor=c, label=lbl) for lbl, c in zip(TAXO_LABELS, TAXO_COLORS)]
    ax.legend(
        handles=handles,
        frameon=False,
        ncol=5,
        fontsize=8.8,
        loc="lower center",
        bbox_to_anchor=(0.5, -0.22),
        handlelength=1.1,
        columnspacing=1.2,
    )
    fig.tight_layout()
    fig.savefig(OUT / "taxonomy.png", dpi=150, bbox_inches="tight")
    plt.close(fig)


# ----------------------------------------------------------------------------
# Fig 4 — per-pair merge-clean heatmap
# ----------------------------------------------------------------------------
def fig_heatmap():
    cmap = LinearSegmentedColormap.from_list("amber_forest", ["#FBF3E2", AMBER_LIGHT, SAGE, FOREST_DEEP])
    fig, ax = plt.subplots(figsize=(7.4, 8.4))
    ax.imshow(HEAT, aspect="auto", cmap=cmap, vmin=0, vmax=100)
    ax.set_xticks(range(len(HEAT_ARMS)))
    ax.set_xticklabels(HEAT_ARMS, fontsize=10)
    ax.set_yticks(range(len(PAIR_LABELS)))
    ax.set_yticklabels(PAIR_LABELS, fontsize=8.5)
    ax.tick_params(length=0)
    for s in ax.spines.values():
        s.set_visible(False)
    for i in range(len(PAIR_LABELS)):
        for j in range(len(HEAT_ARMS)):
            v = HEAT[i][j]
            ax.text(
                j,
                i,
                f"{v}",
                ha="center",
                va="center",
                fontsize=8,
                color="white" if v >= 55 else INK,
            )
    ax.set_title("Per-pair merge-clean rate", fontsize=13.5, fontweight="bold", pad=10, loc="left")
    ax.text(
        0,
        1.008,
        "18 validated pairs x 6 protocols. Darker forest = higher merge-clean rate",
        transform=ax.transAxes,
        fontsize=9.5,
        color=MUTED,
    )
    ax.set_xticks([x - 0.5 for x in range(1, len(HEAT_ARMS))], minor=True)
    ax.set_yticks([y - 0.5 for y in range(1, len(PAIR_LABELS))], minor=True)
    ax.grid(which="minor", color=SURFACE, linewidth=1.5)
    ax.grid(which="major", visible=False)
    fig.tight_layout()
    fig.savefig(OUT / "heatmap.png", dpi=150, bbox_inches="tight")
    plt.close(fig)


